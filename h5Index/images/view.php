<b>注意事项</b><br />
◆ 不能用金属或坚硬物体破坏卷材；<br />
◆ 施工完成，扎钢筋或浇筑混凝土时，应加强对防水层保护；<br />
◆ 不要把卷材施工在有污染、有明水或结冰的基面上；<br />
◆ 施工工序应按步骤进行，不能交叉施工。<br />
