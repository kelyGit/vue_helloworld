				<b>产品介绍</b><br />
JH-W100 湿铺/预铺式聚酯胎防水卷材是我公司采用特殊定制的优质石油沥青为主料，配以活性助剂、增粘树调配而成的自粘胶料，用聚酯布（毡）为增强胎体所制成的可卷曲的片状防水材料。该材料实现了与基面或结构层满粘效果，实现永久性皮肤式防水。<br />
<br />
<b>产品特征</b><br />
◆ 聚酯胎基布作为增强层，耐穿刺、耐硌破、耐撕裂，提高材料强度，有效抵御来自上下表面的损伤和破坏。<br />
◆ 与建筑结构满粘结，有效阻止液态水和水蒸汽进入结构中。<br />
◆ 预铺卷材施工后，可直接铺设钢筋和混凝土，不需设保护层，加快了施工进度。<br />
◆ 湿铺卷材可直接在潮湿基面（无明水）上施工，受雨季天气影响不明显，大大缩短工期。<br />
◆ 抗拉强度高，延伸率大，对基层收缩变形和开裂的适应能力强。<br />
◆ 高温不流淌，低温无裂纹，使用温度范围广。<br />
◆ 持久的粘结性，与基层粘结不脱落、不窜水，搭接缝处自身粘结与卷材同寿命。<br />
◆ 冷施工无须使用明火，施工安全快捷；没有溶剂挥发，更节能环保。<br />
◆ 后续维护简单，即使局部破坏，渗水被锁定在小范围内，不会产生窜水现象。<br />
<br />
<b>适用范围</b><br />
适用于民用建筑防水工程及国家重点工程，尤其是地下室底板、侧墙、顶板、屋面、水池、隧道涵洞及地铁、高铁、铁路等防水工程。<br />
<br />
<b>产品规格<br />
<img alt="" src="/uploads/allimg/151203/1-15120323404H26.jpg" style="width: 800px; height: 85px;" /></b><br />
<br />
<b>施工流程</b><br />
1、预铺法：<br />
基层处理&mdash;局部节点处理&mdash;预铺卷材&mdash;搭接收头、密封处理&mdash;揭除卷材表面隔离膜&mdash;浇筑混凝土。<br />
2、湿铺法：<br />
基层处理&mdash;调制水泥砂浆&mdash;节点、转角处理&mdash;铺贴建材&mdash;搭接收头&mdash;密封处理。<br />
<img alt="" src="/uploads/allimg/151203/1-1512032341563Q.jpg" style="width: 800px; height: 188px;" /><br />
<br />
<b>性能指标</b><br />
产品性能执行指标 GB/T23457-2009 PY类《预铺/湿铺防水卷材》要求。<br />
<img alt="" src="/uploads/allimg/151203/1-15120323425G32.jpg" style="width: 800px; height: 535px;" /><br />
